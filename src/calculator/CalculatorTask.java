package calculator;

public class CalculatorTask {

	/**
	 * The CalculatorTask has 3 methods addition, product, divison to perform operations of calculator
	 * @coder Hema
	 */
	/**
	 * The below method will add two numbers and return their sum as result
	 */

	public  int addition(int number1, int number2)
	{
		return (number1+number2);
	}
	
	/**
	 * The Below method will multiply two numbers and return their product as result
	 */
	
	public  int product(int number1, int number2)
	{
		return (number1*number2);
	}
	
	/**
	 *The below method will add two numbers and return their quotient as result
	 */
	
	public  int divison(int number1, int number2)
	{
		return (number1/number2);
	}

}
